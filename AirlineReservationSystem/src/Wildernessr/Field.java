package Wildernessr;

public class Field {
	//필드
	private int balance = 0; //잔액
	private int price = 0;  //금액
	
	private long cardNum = 0; //카드번호(-없이 숫자만 입력하세요)
	private int Validity = 0; //유효기간 (mmYY)
	private int Installment = 0;//할부
	private int cvcNum = 0; //CVC넘버

	private String cardCompany = null;
	public static final String COMPANY_ACCOUNT = "신한 110-369-681992, 예금주 : 광야"; 

	//생성자
	public Field(int balance, int cardNum, int validity) {
		this.balance = balance;
		this.cardNum = cardNum;
		Validity = validity;
	} 
	public Field() {
	}

	//getter setter
	public int getCvcNum() {
		return cvcNum;
	}
	public void setCvcNum(int cvcNum) {
		this.cvcNum = cvcNum;
	}
		public String getCardCompany() {
		return cardCompany;
	}
	public void setCardCompany(String cardCompany) {
		this.cardCompany = cardCompany;
	}
	public int getInstallment() {
		return Installment;
	}
	public void setInstallment(int installment) {
		Installment = installment;
	}
	public int getBalance() {
		return balance;
	}

	public void setBalance(int balance) {
		this.balance = balance;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public long getCardNum() {
		return cardNum;
	}

	public void setCardNum(long cardNum) {
		this.cardNum = cardNum;
	}

	public int getValidity() {
		return Validity;
	}

	public void setValidity(int validity) {
		Validity = validity;
	}
}
